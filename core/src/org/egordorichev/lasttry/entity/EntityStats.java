package org.egordorichev.lasttry.entity;

public class EntityStats {
    /**
     * Current hit-points out of the total {@link #maxHp maximum}
     */
    public int hp;

    /**
     * Maximum hit-points
     */
    public int maxHp;

    /**
     * Base defense-points
     */
    public int defense;

    /**
     * Base damage-points
     */
    public int damage;
}